package org.menlorobotics.test;

public class Point {
	int x, y, z;
	String data;
	String someMoreData;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getSomeMoreData() {
		return someMoreData;
	}

	public void setSomeMoreData(String someMoreData) {
		this.someMoreData = someMoreData;
	}

}
